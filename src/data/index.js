function getTechList() {
  const res = [];
  for (let i = 0; i < data.length; i++) {
    res.push({ name: data[i].techName, id: data[i].techId });
  }
  return res;
}

function getData(techId) {
  if (techId === "all") {
    return getTechList();
  }
  for (let i = 0; i < data.length; i++) {
    if (data[i].techId === techId) {
      return data[i];
    }
  }

  return null;
}

